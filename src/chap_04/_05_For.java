package chap_04;

public class _05_For {
    public static void main(String[] args) {

        for (int i = 10; i >= 0; i-=2) {
            System.out.println("어서오세요. 나코입니다. : " + i);
        }

        /*
        어서오세요. 나코입니다. : 10
        어서오세요. 나코입니다. : 8
        어서오세요. 나코입니다. : 6
        어서오세요. 나코입니다. : 4
        어서오세요. 나코입니다. : 2
        어서오세요. 나코입니다. : 0
        */

    }
}
